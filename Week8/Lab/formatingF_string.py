"""TASK: Write some code that uses an f-string to calculate then display a message stating
“The area of a rectangle with a width of 104.32 and a height of 15.654 is ….”. Showing the
correct answer at the end of the message."""

width = 104.32
height = 15.654
print(f"The area of a rectangle with a width of {width} and a height of {height} is {width * height:.2f}.")