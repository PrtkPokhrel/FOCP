# Output: <class 'float'>

print(type("Hello"))    
# Output: <class 'str'>

print(type(True))       
# Output: <class 'bool'>

print(type(100 / 5))    
# Output: <class 'float'>

print(type(100 // 5))   
# Output: <class 'int'>

